# arithmetical progression

# the sum of the first n-members of arithmetical progression
def get_sum_of_the_first_n_members_of_arithmetical_progression(a_n, d):
    return ((d * 1 + d * a_n) * a_n) / 2

# the row of the first n-members of arithmetical progression
def get_row_of_the_first_n_members_of_arithmetical_progression(a_n, d):
    list = []
    iter = 0
    for a in range(a_n):
        iter += d
        list.append(iter)
    return list



