# sequences and rows #

# 2n
def get_n_natural_even_numbers_in_a_row(n):
    list = []
    for num in range(n):
        list.append(2 * num)
    return list

# 2n + 1
def get_n_natural_odd_numbers_in_a_row(n):
    list = []
    for num in range(n):
        list.append(2 * num + 1)
    return list

