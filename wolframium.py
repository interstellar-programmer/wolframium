# sequences and rows #

# Get n first natural even (2n) numbers in a row
def get_n_first_natural_even_numbers_in_a_row(n, include_zero = True):
    list = []
    for num in range(n):
        list.append(2 * num)
    if include_zero == True:
        return list
    else:
        list.pop(0)
        list.append(2 * n)
        return list

# Get n first natural odd (2n+1) numbers in a row
def get_n_first_natural_odd_numbers_in_a_row(n):
    list = []
    for num in range(n):
        list.append(2 * num + 1)
    return list

