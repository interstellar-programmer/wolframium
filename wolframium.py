# The sum of the first n-members of arithmetical progression
def get_sum_of_the_first_n_members_of_arithmetical_progression(n, d):
    return ((d * 1 + d * n) * n) / 2

# The row of the first n-members of arithmetical progression
def get_row_of_the_first_n_members_of_arithmetical_progression(n, d):
    list = []
    iter = 0
    for a in range(n):
        iter += d
        list.append(iter)
    return list



